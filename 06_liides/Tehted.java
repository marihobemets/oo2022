public class Tehted {
    public void liida(double x, double y) {
        System.out.println(x + " + " + y + " = " + (x + y));
    }
    public void lahuta(double x, double y) {
        System.out.println(x + " - " + y + " = " + (x - y));
    }
    public void korruta(double x, double y) {
        System.out.println(x + " * " + y + " = " + (x * y));
    }
    public void jaga(double x, double y) {
        System.out.println(x + " / " + y + " = " + (x / y));
    }
}
